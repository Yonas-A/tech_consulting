import pandas as pd

DASH_LINES = "\n" + "-" * 80 + "\n"

# #################################################
# ## 1. Replace missing salary                   ##
# ## 2. Handle invalid salary ("abc")            ##
# ## 3. Filter salary > 6000                     ##
# ## 4. Parse joining_date                       ##
# ## 5. Group by department                      ##
# ## 6. Calculate total payroll                  ##
# ## 7. Add tax column                           ##
# ## 8. Identify high earners                    ##
# #################################################

df = pd.read_csv("./data/employees.csv")
df.info()
# print(DASH_LINES, end="\n\n")


## ------------------------------------------------------------------------- ##
## ------------------------------------------------------------------------- ##
## 1. ------------------ Handle invalid amount for salary ------------------ ##
## ------------------------------------------------------------------------- ##
## ------------------------------------------------------------------------- ##

df["salary"] = pd.to_numeric(df["salary"], errors="coerce")  # invalid → NaN
df["salary"] = df["salary"].fillna(df["salary"].mean())  # fill NaN with mean
print("\nno invalid salary\n\n", df, DASH_LINES, end="\n\n")


## ------------------------------------------------------------------------- ##
## ------------------------------------------------------------------------- ##
## 2. --------------------- remove rows with empty ages -------------------- ##
## ------------------------------------------------------------------------- ##
## ------------------------------------------------------------------------- ##

df["age"] = pd.to_numeric(df["age"], errors="coerce")  # invalid to NaN
df = df.dropna(subset=["age"])  # drop NaN ages
print(df, DASH_LINES, end="\n\n")


## ------------------------------------------------------------------------- ##
## ------------------------------------------------------------------------- ##
## 3. ------------------------ filter salary > 6000 ------------------------ ##
## ------------------------------------------------------------------------- ##
## ------------------------------------------------------------------------- ##

df_high_salary = df.query("salary > 6000").reset_index(drop=True)
print("salary > 6000\n\n", df_high_salary, DASH_LINES, end="\n\n")


## ------------------------------------------------------------------------- ##
## ------------------------------------------------------------------------- ##
## 4. ------------------------- parse joining_date ------------------------- ##
## ------------------------------------------------------------------------- ##
## ------------------------------------------------------------------------- ##

df["joining_date"] = pd.to_datetime(df["joining_date"], errors="coerce")
df = df.dropna(subset=["joining_date"])  # drop nan joining dates
print(df, DASH_LINES, end="\n\n")

df.info()
print(DASH_LINES, end="\n\n")


## ------------------------------------------------------------------------- ##
## ------------------------------------------------------------------------- ##
## 5. ------------------------- Group by department -------------------------##
## ------------------------------------------------------------------------- ##
## ------------------------------------------------------------------------- ##

df_department = (
    df.groupby("department")["salary"]
    .sum()
    .reset_index(name="total_salary")  # rename aggregated column
    .sort_values(by="total_salary", ascending=False)  # sort descending
)

print("salary by department\n\n", df_department, DASH_LINES, end="\n\n\n")


## ------------------------------------------------------------------------- ##
## --------------------------- Summary by region --------------------------- ##
## ------------------------------------------------------------------------- ##
summary = (
    df.groupby("region")
    .agg(
        employee_count=("employee_id", "count"),
        avg_salary=("salary", "mean"),
        avg_age=("age", "mean"),
    )
    .reset_index()
    .sort_values(by="avg_salary", ascending=False)  # sort descending by avg_salary
    .reset_index(drop=True)  # optional: clean sequential index
)
print("Regional summary\n\n", summary, DASH_LINES, end="\n\n\n")

## ------------------------------------------------------------------------- ##
## ------------------------------------------------------------------------- ##
## 6. ----------------------- Calculate total payroll ---------------------- ##
## ------------------------------------------------------------------------- ##
## ------------------------------------------------------------------------- ##
total_payroll = df["salary"].sum()
print("total Payroll:", total_payroll, DASH_LINES, end="\n\n\n")

## ------------------------------------------------------------------------- ##
## ------------------------------------------------------------------------- ##
## 7. --------------------------- Add tax column --------------------------- ##
## ------------------------------------------------------------------------- ##
## ------------------------------------------------------------------------- ##

df["tax"] = df["salary"] * 0.15
print("tax\n\n", df, DASH_LINES, end="\n\n")

## ------------------------------------------------------------------------- ##
## ------------------------------------------------------------------------- ##
## 8. ------------------------ Identify high earners ----------------------- ##
## ------------------------------------------------------------------------- ##
## ------------------------------------------------------------------------- ##
df["high_earner"] = df["salary"].apply(lambda x: "Yes" if x > 6500 else "No")
print("high earners\n\n", df, DASH_LINES, end="\n\n")
